import type { AppProps } from "next/app";
import getConfig from "next/config";
import Image from "next/image";
import { useState } from "react";
import { Configuration, OpenAIApi } from "openai";
import imageIcon from "../assets/imageIcon.png";
import "./App.css";

export default function App({ Component, pageProps }: AppProps) {
  const [imageResult, setImageResult] = useState("");

  const { publicRuntimeConfig } = getConfig();
  const apiKey =
    typeof publicRuntimeConfig !== "undefined" && publicRuntimeConfig.apiKey
      ? publicRuntimeConfig.apiKey
      : process.env.API_KEY;

  if (!apiKey) {
    throw new Error("apiKey is not defined");
  }

  const configuration = new Configuration({ apiKey });
  const openai = new OpenAIApi(configuration);

  const generateImage = async () => {
    try {
      const response = await openai.createImage({
        prompt: "happy heron riding red bicycle on a stone path",
        n: 1,
        size: "256x256",
      });

      const imageData = response.data;
      const imageUrl = imageData.data[0].url;
      console.log("imageUrl: ", imageUrl);

      setImageResult(imageUrl || "");
    } catch (error) {
      console.log(error);
    }
  };

  return (
    <div className="app">
      <h2>Create images with your mind and DALL-E</h2>
      <textarea
        className="app-input"
        placeholder="Create any type of image from your description. Use as much details as you like."
      />
      <button onClick={generateImage}>Generate Image</button>
      {imageResult ? (
        <Image
          src={imageResult}
          alt="Image generated by AI"
          width={300}
          height={300}
          className="app-result"
        />
      ) : (
        <Image
          src={imageIcon}
          alt="Image placeholder"
          width={256}
          height={256}
        />
      )}
    </div>
  );
}
